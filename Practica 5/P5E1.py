#P5E1 JAVIER DURAN Imprimir una tabla de valores con for y range


for num in range(10):
    print(num)
    
for num in range(2,20,2):
    print(num)
    
for num in range (20,40,2):
    print(num)
    
for num in range(10,34,4):
    print(num)
    
for num in reversed(range(0,45,5)):
    print(num)